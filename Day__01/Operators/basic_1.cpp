int main(){
  cout << "Arithmetic Operators:------" << endl;
  cout << "+ ---> a+b  Addition" << endl;
  cout << "- ---> a-b  Subtraction" << endl;
  cout << "* ---> a*b  Multiplication" << endl;
  cout << "/ ---> a/b  Division" << endl;
  cout << "% ---> a%b  Modulus (gives remainder)" << endl << endl;

  cout << " LETS SEE USE :----------" << endl;
  int a = 3, b = 7;
  cout << "Sum = " << a + b << endl; // 10
  cout << "Subtract = " << a - b << endl; // -4
  cout << "Multiply = " << a * b << endl; // 21
  cout << "Divide = " << a / b << endl; // 0
  cout << "Modulus = " << a % b << endl; // 3
}