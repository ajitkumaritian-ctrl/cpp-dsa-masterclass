#include<iostream>
using namespace std;
int main(){
  // % --> Its modulus which gives remainder
  // eg--> 4 % 2 = 0; 4 % 3 = 1;

  cout << "7 % 2 = 1" << "---> " << 7 % 2 <<endl; // 1
  cout << "2 % 7 = 2" << "---> " << 2 % 7 <<endl; // 2
  cout << "7 % -2  = 7 % 2" << "----> " << 7 % -2<<endl; // 1
  cout << "-7 % 2 = -(7 % 2)" << "---> " << -7 % 2 <<endl; // -1

  
}